#{
  Normal Distribution N(mu, sigma):
  - normpdf, normcdf
  - norminv(x, mu, sigma) (the inverse of CDF, i.e., the quantile at x) 
 
  Student Distribution T(n):
  - tpdf, tcdf
  - tinv(x, n)
 
  Chi Square Distribution CS(n):
  - chi2pdf, chi2cdf
  - chi2inv(x, n)  

  Fischer Distribution F(m, n):
  - fpdf, fcdf
  - finv(x, m, n)
